#include "calc.h"
/**
* get_op_func - returns the pointer to the functions that matches
* the passed string operator
* @s: char* - the operator of the function we want
* Return: int(*)(int,int) - the operator function
*/
int (*get_op_func(char *s))(int, int)
{
	op_t ops[] = {
	{"+", op_add},
	{"-", op_sub},
	{"*", op_mul},
	{"/", op_div},
	{"%", op_mod},
	{NULL, NULL}
	};
	int i;

	i = 0;
	while (i < 5)
	{
		if (strcmp(ops[i].op, s) == 0)
		{
			return (ops[i].f);
		}
		i++;
	}
	return (NULL);

}
